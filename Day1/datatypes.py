x= "hello world"
print(x)   # this is the type of string

x= 10
print(x)   #this is the type of integer

x= 20.8
print(x) # this is the type of float

x= 1j
print(x) # this is the type of complex number

x= ["apple", "banana", "cherry"]
print(x) # this is the type of list

x= ("apple", "banana", "cherry")
print(x) # this is the type of tuple

x= range(6)
print(x) # this is the type of range

x= {"name": "John", "age": 30}
print(x) # this is the type of dictionary

x= {"apple", "banana", "cherry"}
print(x) # this is the type of set

x= frozenset({"apple", "banana", "cherry"})
print(x) # this is the type of frozenset

x= True
print(x) # this is the type of boolean

x= b"Hello"
print(x) # this is the type of bytes

x= bytearray(5)
print(x) # this is the type of bytearray

x= memoryview(bytes(5))
print(x) # this is the type of memoryview

x= None
print(x) # this is the type of NoneType

